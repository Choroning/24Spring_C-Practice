#include <stdio.h>

int main()
{
	int vOne = 13;
	int vTwo = 3;

	printf("%d + %d = %d\n", vOne, vTwo, vOne + vTwo);
	printf("%d - %d = %d\n", vOne, vTwo, vOne - vTwo);
	printf("%d * %d = %d\n", vOne, vTwo, vOne * vTwo);
	printf("%d / %d = %d\n", vOne, vTwo, vOne / vTwo);
	printf("%d %% %d = %d\n", vOne, vTwo, vOne % vTwo);
}